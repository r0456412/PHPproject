


<h1 style="text-align: center;">Help</h1>
<p style="text-align: center;">Welcome to the help page, here we show you for every kind of user what he can do in the application</p>

<h3>Guest</h3>
<p>As a guest you first have to get a mail to register yourself. Once you did that you can use the login function to access the guest homepage. On the homepage your have different things your can do. You can give your wishes that you want during your stay and if you want to change any of these wishes, that is possible too. Than you have one more function where you can give all the information about the lecture you are going to give. That's it for the guests.</p>

<h3>Supervicer</h3>
<p>As a superviser you will get your own login. Once you have logged in you will be redirected to the planning. There you have the option to submit yourself to supervise a lecture. More a supervisor doesn't have to do.</p>

<h3>Student</h3>
<p>As a student you don't need a login. On your Toledo account you will get a link that will redirect you to the planning. Here you can see for every day of the international days the lectures for every specialization in the IT-factory.</p>

